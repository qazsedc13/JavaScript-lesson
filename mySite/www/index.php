<?php
	$myNumber = 45;
	$float = -3.25;
	$string = "Hello World!";
	$bool = false;
	
	echo "Переменная myNumber = $myNumber";// в одинарных кавычках выведется как текст, без подставновки значения переменных
	echo "<br />"; 
	echo "Переменная float = $float";
	echo "<br />"; 
	echo "Переменная string = $string";
	echo "<br />"; 
	echo "Переменная bool = $bool";//true выводится как 1, а false выводится как пустое место
	echo "<br />";    
?>